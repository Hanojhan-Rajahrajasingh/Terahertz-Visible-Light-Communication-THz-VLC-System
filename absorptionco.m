
function k=absorptionco(freq)
     
c = 3*10^8;

c0 = -6.36*10^-3;                    
c1 = 9.06*10^-14;                    % Per Hz^1
c2 = -3.94*10^-25;                   % Per Hz^2
c3 = 5.54*10^-37;                    % Per Hz^3

si = 0.5;
p = 101325  ;                      %Pa

q1 = 6.1121;
q2 = 1.0007;
q3 = 3.46*10^-6;
q4 = 17.502;
q5 = 273.15;
q6 = 32.18;

t = 296;
fi_h = 1013.25 ;                   % hPa (1 hPa = 100 Pa)
pw = q1*(q2+q3*fi_h)*exp((q4*(t-q5))/(t-q6));
v = (si*pw)/(100*p);

x = (0.2205*v*((0.1303*v)+0.0294))/(((0.4093*v + 0.0925)^2 + (((100*freq)/c)-10.835)^2));
y = (2.014*v*((0.1702*v)+0.0303))/((0.537*v + 0.0956)^2 + (((100*freq)/c)-12.664)^2);

z = c0 + (c1*freq) + (c2*freq^2) + (c3*freq^3);
k = x+y+z;
